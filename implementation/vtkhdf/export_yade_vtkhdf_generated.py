def export_vtkhdf(filename):
    """Export the current YADE simulation state to an ON-DEM VTKHDF file.

    Generated from schema: schema/
    Mapping file:          yade_mapping.json

    Parameters
    ----------
    filename : str
        Output file path (should end in .vtkhdf or .hdf).
    """
    with h5py.File(filename, "w") as f:

        # 1. VTK PolyData for spheres (/VTKHDF/)
        n_spheres = _export_spheres(f)

        # 2. Scene metadata
        _export_scene(f)

        # 3. Materials
        _export_materials(f)

        # 4. Non-sphere bodies
        _export_nonsphere_bodies(f)

        # 5. Interactions
        _export_interactions(f)

        # Top-level metadata
        f.attrs["file_format"]         = "ON-DEM VTKHDF"
        f.attrs["file_format_version"] = "0.1-draft"
        f.attrs["source_code"]         = "YADE"
        f.attrs["schema_dir"]          = "schema/"
        f.attrs["mapping_file"]        = "yade_mapping.json"

    n_intrs = sum(1 for i in O.interactions if i.isReal)
    print(f"[export_vtkhdf] Exported {n_spheres} spheres, {n_intrs} interactions "
          f"at t={O.time:.6g} to \'{filename}\'")


if __name__ == "__main__":
    out = sys.argv[1] if len(sys.argv) > 1 else "output.vtkhdf"
    export_vtkhdf(out)
