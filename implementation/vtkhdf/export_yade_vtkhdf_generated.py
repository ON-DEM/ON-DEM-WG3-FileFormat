def _export_spheres(f):
    """Write sphere particles to /VTKHDF/ PointData — generated from schema."""

    # --- Collect sphere data ---
    _material_id_list = []
    _clump_id_list = []
    _body_id_list = []
    _position_list = []
    _velocity_list = []
    _orientation_list = []
    _angular_velocity_list = []
    _mass_list = []
    _inertia_list = []
    _volume_list = []
    _radius_list = []

    for b in O.bodies:
        if b is None: continue
        if type(b.shape).__name__ != 'Sphere': continue

        try: _material_id_list.append(b.material.id)
        except: _material_id_list.append(None)
        try: _clump_id_list.append(b.clumpId)
        except: _clump_id_list.append(None)
        try: _body_id_list.append(b.id)
        except: _body_id_list.append(None)
        # UNMAPPED: body_state
        # UNMAPPED: body_shape
        try: _position_list.append(b.state.pos)
        except: _position_list.append(None)
        try: _velocity_list.append(b.state.vel)
        except: _velocity_list.append(None)
        try: _orientation_list.append(b.state.ori)
        except: _orientation_list.append(None)
        try: _angular_velocity_list.append(b.state.angVel)
        except: _angular_velocity_list.append(None)
        try: _mass_list.append(b.state.mass)
        except: _mass_list.append(None)
        try: _inertia_list.append(b.state.inertia)
        except: _inertia_list.append(None)
        try: _volume_list.append(_sphere_volume(b))
        except: _volume_list.append(None)
        # UNMAPPED: color
        try: _radius_list.append(b.shape.radius)
        except: _radius_list.append(None)

    N = len(_body_id_list)
    if N == 0:
        print("[export_vtkhdf] Warning: no sphere bodies found")
        return 0

    # --- Write /VTKHDF/ VTK PolyData structure ---
    vtk = f.require_group("VTKHDF")
    vtk.attrs.create("Type",    data=np.bytes_("PolyData"))
    vtk.attrs.create("Version", data=np.array([2, 0], dtype=np.int64))

    # Points = sphere centres
    pos_arr = np.array([[p[0],p[1],p[2]] for p in _position_list], dtype=np.float64)
    vtk.create_dataset("Points", data=pos_arr)
    vtk.create_dataset("NumberOfPoints", data=np.array([N], dtype=np.int64))

    # Empty cell structures (required by VTK HDF spec for PolyData)
    for _sec in ["Verts","Lines","Polygons","Strips"]:
        _sg = vtk.require_group(_sec)
        _sg.create_dataset("NumberOfCells",           data=np.array([0], dtype=np.int64))
        _sg.create_dataset("NumberOfConnectivityIds", data=np.array([0], dtype=np.int64))
        _sg.create_dataset("Offsets",                 data=np.array([0], dtype=np.int64))
        _sg.create_dataset("Connectivity",            data=np.array([], dtype=np.int64))

    pd = vtk.require_group("PointData")

    # --- Write per-particle datasets ---
    # [mandatory] material_id: int [$-$]
    pd.create_dataset("material_id", data=np.array(_material_id_list, dtype=np.int32))

    # [mandatory] clump_id: int [$-$]
    pd.create_dataset("clump_id", data=np.array(_clump_id_list, dtype=np.int32))

    # [mandatory] body_id: int [$-$]
    pd.create_dataset("body_id", data=np.array(_body_id_list, dtype=np.int32))

    # [mandatory] position: Vector3 [$L$]
    pd.create_dataset("position", data=np.array([[v[0],v[1],v[2]] for v in _position_list], dtype=np.float64))

    # [mandatory] velocity: Vector3 [$L T^{-1}$]
    pd.create_dataset("velocity", data=np.array([[v[0],v[1],v[2]] for v in _velocity_list], dtype=np.float64))

    # [mandatory] orientation: Quaternion [$-$]
    pd.create_dataset("orientation", data=np.array([[q[1],q[2],q[3],q[0]] for q in _orientation_list], dtype=np.float64))

    # [mandatory] angular_velocity: Vector3 [$T^{-1}$]
    pd.create_dataset("angular_velocity", data=np.array([[v[0],v[1],v[2]] for v in _angular_velocity_list], dtype=np.float64))

    # [mandatory] mass: float [$M$]
    pd.create_dataset("mass", data=np.array(_mass_list, dtype=np.float64))

    # [mandatory] inertia: Matrix3 [$M L^{2}$]
    pd.create_dataset("inertia", data=np.array([_mat3_flat(iv) for iv in _inertia_list], dtype=np.float64))

    # [mandatory] volume: float [$L^{3}$]
    pd.create_dataset("volume", data=np.array(_volume_list, dtype=np.float64))

    # [mandatory] radius: float 
    pd.create_dataset("radius", data=np.array(_radius_list, dtype=np.float64))

    return N


