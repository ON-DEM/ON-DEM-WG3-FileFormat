def _get_gravity():
    """YADE gravity lives on NewtonIntegrator, not on O directly."""
    for eng in O.engines:
        if type(eng).__name__ == "NewtonIntegrator":
            g = eng.gravity
            return [g[0], g[1], g[2]]
    return [0.0, 0.0, 0.0]

def _sphere_volume(b):
    r = b.shape.radius
    return 4.0 / 3.0 * 3.141592653589793 * r**3

def _vec3(v):
    return np.array([v[0], v[1], v[2]], dtype=np.float64)

def _quat(q):
    """YADE: q[0]=w → store as [x,y,z,w]"""
    return np.array([q[1], q[2], q[3], q[0]], dtype=np.float64)

def _mat3_flat(iv):
    """YADE inertia is a Vector3 of principal moments → diagonal 3x3, row-major."""
    return np.array([iv[0],0,0, 0,iv[1],0, 0,0,iv[2]], dtype=np.float64)


def _export_scene(f):
    """Write /ONDEM/Scene — generated from scene schema."""
    grp = f.require_group("ONDEM/Scene")

    #  units: List[str] 
    _dt = h5py.special_dtype(vlen=str)
    _ds = grp.create_dataset("units", (len(['second', 'meter', 'kilogram']),), dtype=_dt)
    for _ii, _v in enumerate(['second', 'meter', 'kilogram']): _ds[_ii] = _v

    #  timestep: float [T]
    grp.attrs["timestep"] = O.dt

    #  time: float [T]
    grp.attrs["time"] = O.time

    #  gravity: Vector3 [L/T²]
    _v = _get_gravity()
    grp.create_dataset("gravity", data=np.array([_v[0],_v[1],_v[2]], dtype=np.float64))


